<?php 

$num1 = readline('Digite o primeiro numero: ');
$num2 = readline('Digite o segundo numero: ');

$soma = $num1 + $num2;
echo "Soma: " . $soma . PHP_EOL; // Adição

$subtracao = $num1 - $num2;
echo "Subtracao: " . $subtracao . PHP_EOL; // Subtração

$multiplicacao = $num1 * $num2;
echo "Multiplicacao: " . $multiplicacao . PHP_EOL; // Multiplicação

$divisao = $num1 / $num2;
echo "Divisao: " . $divisao . PHP_EOL; // Divisão

$modulo = $num1 % $num2;
echo "Modulo: " . $modulo . PHP_EOL; // Módulo ou Resto da Divisão

//