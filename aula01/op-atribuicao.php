<?php 

$num = readline('Digite o numero: ');

$soma = readline('Digite o numero soma: ');
$num += $soma;
echo $num . PHP_EOL;

$subt = readline('Digite o numero subt: ');
$num -= $subt;
echo $num . PHP_EOL;

$mult = readline('Digite o numero mult: ');
$num *= $mult;
echo $num . PHP_EOL;

$div = readline('Digite o numero div: ');
$num /= $div;
echo $num . PHP_EOL;

$mod = readline('Digite o numero mod: ');
$num %= $mod;
echo $num . PHP_EOL;